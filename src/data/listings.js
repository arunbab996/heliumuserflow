export function formatRent(amount) {
  if (amount >= 100000) { return `₹${(amount / 100000).toFixed(1).replace('.0', '')} L` }
  return `₹${(amount / 1000).toFixed(0)}K`
}

export function formatRentFull(amount) {
  return `₹${amount.toLocaleString('en-IN')}`
}
